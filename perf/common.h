#define xtm_fail(expr, result) do {                                 \
        fprintf(stderr, "Test failed: %s is %s at %s:%d, in function '%s'\n",\
                expr, result, __FILE__, __LINE__, __func__);            \
        exit(-1);                                                       \
} while (0)
#define xtm_fail_unless(expr) if (!(expr)) xtm_fail(#expr, "false")

